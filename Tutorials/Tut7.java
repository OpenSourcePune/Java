import java.util.Scanner;
public class Tut7
{
	public static void main(String[] args) {
		
		//While loops
		int a = 1;
		while (a<=10)
		{
			System.out.println(a);
			a++;
		}
		//End of loop
		
		System.out.println(" ");
		
		//Decreasing while loops
		int y = 10;
		while (y!=0)
		{
			System.out.println(y+" Hello there");
			y--;
		}
		//End of loop
		
		//Do while loops
		int b = 0;
		do {
			System.out.println("Yabadabadoo "+b);
			b++;
		}while(b==10);
		//Do while loops execute at least once
		
		
	}
}